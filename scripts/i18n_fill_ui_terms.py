def _decode_po_string(lines: list[str], start: int, key: str) -> tuple[str, int]:
    """Decode msgid/msgstr and return (decoded, next_index)."""
    first = lines[start]
    prefix = f"{key} "
    raw_parts = [first[len(prefix) :].rstrip("\n")]
    idx = start + 1
    while idx < len(lines) and lines[idx].startswith('"'):
        raw_parts.append(lines[idx].strip())
        idx += 1
    text = "".join(ast.literal_eval(p) for p in raw_parts)
    return text, idx


def _po_quote(text: str) -> str:
    escaped = text.replace("\\", "\\\\").replace('"', '\\"')
    return f"\"{escaped}\""


def fill_po_file(path: Path, mapping: dict[str, str]) -> int:
    lines = path.read_text(encoding="utf-8").splitlines(keepends=True)
    out: list[str] = []
    i = 0
    changes = 0

    while i < len(lines):
        if not lines[i].startswith("msgid "):
            out.append(lines[i])
            i += 1
            continue

        msgid_start = i
        msgid_text, after_msgid = _decode_po_string(lines, i, "msgid")

        # Copy msgid block unchanged.
        out.extend(lines[msgid_start:after_msgid])
        i = after_msgid

        # Skip plural entries.
        if i < len(lines) and lines[i].startswith("msgid_plural "):
            out.append(lines[i])
            i += 1
            continue

        if i < len(lines) and lines[i].startswith("msgstr "):
            msgstr_text, after_msgstr = _decode_po_string(lines, i, "msgstr")
            translated = mapping.get(msgid_text)
            if translated and msgstr_text != translated:
                out.append(f"msgstr {_po_quote(translated)}\n")
                changes += 1
            else:
                out.extend(lines[i:after_msgstr])
            i = after_msgstr
            continue

    if changes:
        path.write_text("".join(out), encoding="utf-8")
    return changes


